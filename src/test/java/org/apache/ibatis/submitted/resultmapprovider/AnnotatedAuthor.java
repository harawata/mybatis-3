package org.apache.ibatis.submitted.resultmapprovider;

import java.time.LocalDate;

public class AnnotatedAuthor {
  private Long id;
  private String name;
  @MyField(column = "date_of_birth")
  private LocalDate dateOfBirth;

  public AnnotatedAuthor(@MyField(id = true) Long id, String name) {
    this.id = id;
    this.name = name;
  }

  public Long getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public LocalDate getDateOfBirth() {
    return dateOfBirth;
  }
}
